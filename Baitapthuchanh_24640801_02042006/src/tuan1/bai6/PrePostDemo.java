package tuan1.bai6;

public class PrePostDemo {
	public static void main(String[] args) {
		int i = 3;
		i++;
		System.out.println(i);
		++i;
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(--i);
		System.out.println(i--);
		System.out.println(i);
	}
	/*i++ là dùng i rồi +1
	 * ++i là +1 rồi mới dùng i
	 * tương tự với i-- và --i*/
}


